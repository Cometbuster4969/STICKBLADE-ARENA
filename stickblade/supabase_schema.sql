-- STICKBLADE ARENA — Supabase schema
-- Run this in: Supabase Dashboard -> SQL Editor -> New query -> Run
-- Safe to run on a FRESH project AND on an existing populated database.
-- Then create a Storage bucket named: replays (private is fine).

-- ============================================================================
-- 1. CREATE TABLES (no-op on existing tables, full schema on fresh installs)
-- ============================================================================
create table if not exists matches (
    id          text primary key,
    created     double precision,
    model_a     text,
    model_b     text,
    sharp       text,
    weapon      text default 'sword',     -- sword | dagger | spear | flail | bow
    -- Tier-S commit 2: mode + arena are part of the eval axis. Different
    -- control regime (macro vs joint) and different physics (normal / ice /
    -- low_gravity) = different rating cell. Persisted per-match so votes
    -- route to the right (model, sharp, weapon, mode, arena) elo row.
    mode        text default 'macro',     -- macro | joint
    arena       text default 'normal',    -- normal | ice | low_gravity
    status      text,                     -- queued | running | done | error
    winner_side text,                     -- a | b | draw (CANVAS side: a=green, b=blue)
    method      text,
    turns       integer,
    error       text,
    blind       boolean default true,
    voted       boolean default false,
    -- True = the user's model_a was rendered as the BLUE (Fighter B) ragdoll.
    -- Server picks this at queue time so the picker can't leak identity.
    flip        boolean default false,
    -- 2-sentence post-fight commentary/roast generated by a 3rd LLM
    commentary  text
);

create table if not exists votes (
    id        text primary key,
    match_id  text references matches(id),
    created   double precision,
    choice    text                          -- a | b | draw (CANVAS side)
);

-- Per-weapon, per-sharp, per-mode, per-arena Elo leaderboards.
-- PK is (model, sharp, weapon, mode, arena) — see Tier-S commit 2.
-- On fresh installs this creates the correct PK directly; on existing
-- deployments the migration block below promotes the PK safely.
create table if not exists elo (
    model   text,
    sharp   text,
    weapon  text default 'sword',
    mode    text default 'macro',    -- macro | joint
    arena   text default 'normal',   -- normal | ice | low_gravity
    rating  double precision default 1000,
    wins    integer default 0,
    losses  integer default 0,
    draws   integer default 0,
    primary key (model, sharp, weapon, mode, arena)
);

create table if not exists tournaments (
    id            text primary key,
    created       double precision,
    name          text,
    size          integer,
    weapon        text default 'sword',
    sharp         text,
    arena         text default 'normal',
    mode          text default 'macro',
    status        text,                 -- queued | running | done | error
    current_round integer default 0,
    winner_model  text,
    models        text,                 -- JSON array of model ids in seed order
    error         text
);

create table if not exists tournament_matches (
    id            serial primary key,
    tournament_id text references tournaments(id),
    round         integer,
    slot          integer,
    match_id      text,
    model_a       text,
    model_b       text,
    winner_model  text,
    unique (tournament_id, round, slot)
);

